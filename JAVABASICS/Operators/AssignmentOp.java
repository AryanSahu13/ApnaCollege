public class AssignmentOp{
    public static void main(String args[]){
        //Relational Operators
        int A = 10; int B = 5;
        System.out.println("Sum is: " + (A += B)); //Sum
        System.out.println("Sub is: " + (A -= B)); //Subtraction
        System.out.println("Mul is: " + (A *= B)); //Multiplication
        System.out.println("Div is: " + (A /= B)); //Division
        System.out.println("Rem is: " + (A %= B)); //Remainder
    }    
}
