public class RelationalOp{
    public static void main(String args[]){
        //returns true or false
        int A = 10;
        int B = 5;
        System.out.println(A==B);                    // checks if both are equal
        System.out.println(A!=B);                    // checks if both are not equal
        System.out.println(A>=B);                    // checks if A is greater than or equal to B
        System.out.println(A<=B);                    // Checks if A is smaller than or equal to B
        System.out.println(A>B);                     // Checks if A is greater than B
        System.out.println(A<B);                     // Checks if A is smaller than B
    }
}
